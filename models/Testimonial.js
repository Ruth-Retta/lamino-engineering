import mongoose from 'mongoose';

const TestimonialSchema = new mongoose.Schema({
    author: {
        type: String,
        required: true,
        trim: true,
    },
    position: {
        type: String,
        required: true,
        trim: true,
    },
    content: {
        type: String,
        required: true,
        trim: true,
    },
    image: {
        type: String,
        required: true,
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
});

export default mongoose.models.Testimonial || mongoose.model('Testimonial', TestimonialSchema);
