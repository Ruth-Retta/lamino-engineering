import mongoose from 'mongoose';

const CareerSchema = new mongoose.Schema({
    position: {
        type: String,
        required: true,
    },
    startDate: {
        type: Date,
        required: true,
    },
    endDate: {
        type: Date,
        required: true,
    },
    description: {
        type: String,
        required: true,
    },
    requirements: {
        type: [String],
        required: true,
    },
});

export default mongoose.models.Career || mongoose.model('Career', CareerSchema);
